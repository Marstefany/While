using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp3
{
    internal class ContagemRegressiva
    {
        
        public static void Main(string[] args)
        {
            int contadora = 11;
            do
            {
                contadora = contadora - 1;
                Console.WriteLine(contadora);
            } while (contadora != 1);
        }
    }
}
    

